/**
 * Get token by ID
 */
export function getTokenById(id: string): TokenInfo | null {
  return ALL_TOKENS[id] || null;
}

/**
 * Get token by symbol
 */
export function getTokenBySymbol(symbol: string): TokenInfo | null {
  const upperSymbol = symbol.toUpperCase();
  return (
    Object.values(ALL_TOKENS).find(
      (token) => token.symbol.toUpperCase() === upperSymbol
    ) || null
  );
}

/**
 * Get all tokens as an array
 */
export function getAllTokens(): TokenInfo[] {
  return Object.values(ALL_TOKENS);
}

/**
 * Get verified tokens only
 */
export function getVerifiedTokens(): TokenInfo[] {
  return Object.values(ALL_TOKENS).filter((token) => token.isVerified);
}
